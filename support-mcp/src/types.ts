/**
 * Обобщённые доменные типы тикет-системы — НЕ форма GitHub. Ассистент поддержки общается только с
 * ними, поэтому под любой трекер/CRM (GitHub Issues, GitLab, Jira, Zendesk) достаточно нового
 * провайдера, отдающего эти типы, — код бота не меняется.
 */

/** Пользователь (автор тикета или комментария). */
export interface TicketUser {
  /** Логин/идентификатор в системе. */
  login: string;
  /** Человекочитаемое имя, если есть. */
  displayName?: string;
}

/** Комментарий в треде тикета (реплика диалога поддержки). */
export interface TicketComment {
  /** Идентификатор комментария в системе. */
  id: number;
  author: TicketUser;
  body: string;
  /** Момент создания (ISO). */
  createdAt: string;
  /** Оставлен ли комментарий ботом (распознаётся по скрытому маркеру) — для защиты от петли. */
  isBot: boolean;
}

/** Тикет = обращение пользователя (для GitHub — issue). */
export interface Ticket {
  /** Адресация тикета в системе (для GitHub — номер issue, напр. 42). */
  id: number;
  title: string;
  /** Тело первого сообщения (описание проблемы). */
  body: string;
  author: TicketUser;
  /** Метки: продуктовая область/статус/приоритет. */
  labels: string[];
  /** Состояние (open/closed/…). */
  state: string;
  /** Ссылка на тикет. */
  url: string;
}
