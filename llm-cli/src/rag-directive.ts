import type { ToolSpec } from '../../core/src/index.ts';

/** Суффикс имени инструмента поиска по документам (с учётом неймспейса «сервер__search_docs»). */
const SEARCH_TOOL_SUFFIX = 'search_docs';

/** Подходит ли инструмент под RAG-поиск (по суффиксу имени). */
export function isSearchDocsTool(name: string): boolean {
  return name.endsWith(SEARCH_TOOL_SUFFIX);
}

/**
 * Компактная сводка результата search_docs для показа в чате: строка-трасса стадий (🔎) и заголовки
 * найденных фрагментов `[n] source › file › section (score)` — без тел фрагментов (те и так уходят
 * модели в контекст). Так пользователь ВИДИТ, что и в каком порядке нашлось при разных режимах
 * (rerank/rewrite/порог), а не только сглаженный финальный ответ.
 */
export function formatRagResultForDisplay(result: string): string {
  return result
    .split('\n')
    .filter(line => line.startsWith('🔎') || line.startsWith('⚠') || /^\[\d+\]/.test(line))
    .join('\n');
}

/**
 * Директива агенту при доступном RAG-поиске: вызвать search_docs на вопросы о репозитории/сайте/
 * папке или проиндексированных данных и отвечать СТРОГО по фрагментам в обязательном формате из трёх
 * секций (Ответ/Источники/Цитаты) с дословными цитатами; при пустом/слабом контексте — «не знаю» и
 * просьба уточнить. `compact` (для длинного чата) просит короткие источники и одну цитату. Нет
 * инструмента — null.
 */
export function ragSearchDirective(specs: ToolSpec[], compact = false): string | null {
  if (!specs.some(spec => isSearchDocsTool(spec.name))) {
    return null;
  }
  const citations = compact
    ? 'Цитаты: одна короткая ДОСЛОВНАЯ выдержка из фрагмента, подтверждающая ответ.'
    : 'Цитаты: ДОСЛОВНЫЕ выдержки из найденных чанков (копируй текст без изменений), ' +
      'по одной короткой на каждый источник.';
  return (
    'Тебе доступен инструмент поиска по документам (search_docs) с индексацией на лету. Если вопрос ' +
    'про репозиторий, сайт или папку (в вопросе есть github.com, URL или путь) либо ранее ' +
    'проиндексированные данные — ВЫЗОВИ search_docs (source из вопроса, если указан) и отвечай ' +
    'СТРОГО по возвращённым фрагментам.\n' +
    'Формат ответа ОБЯЗАТЕЛЕН — ровно три секции:\n' +
    'Ответ: краткий ответ по фрагментам.\n' +
    'Источники: по одному на строку в виде «source › section · chunk_id», только реально найденные.\n' +
    citations +
    '\nНе выдумывай источники и цитаты: цитата обязана быть дословной подстрокой найденного ' +
    'фрагмента. Если релевантных фрагментов нет ИЛИ результат помечен «⚠ Низкая уверенность» — не ' +
    'придумывай ответ: напиши «Не знаю: в источниках недостаточно контекста» и попроси уточнить ' +
    'запрос (без секций Источники/Цитаты).'
  );
}
