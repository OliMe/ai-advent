/**
 * Дисциплина инструментов: не выдумывать результаты вызовов. Без неё модель иногда «рапортует»
 * о созданной/удалённой задаче, не вызвав инструмент, и придумывает идентификаторы.
 */
export const TOOL_HONESTY_DIRECTIVE =
  'КРИТИЧЕСКИ ВАЖНО про инструменты. Если пользователь просит выполнить действие (создать/' +
  'поставить напоминание или задачу, удалить, отменить, запустить, изменить расписание) — ты ОБЯЗАН ' +
  'вызвать соответствующий инструмент (schedule_task/cancel_task/run_now/…) ПРЕЖДЕ чем сообщать о ' +
  'результате. НЕ пиши «создал/добавил/удалил/готово» и НЕ показывай ID/таблицы, если инструмент в ' +
  'этом ответе НЕ вызывался. Все идентификаторы и статусы бери ТОЛЬКО из ответов инструментов — ' +
  'НИКОГДА не придумывай их.';

/**
 * Слова-маркеры ЗАЯВЛЕННОГО (выполненного) действия — прошедшее время/перфект. Только такие формы,
 * не инфинитивы: «добавил/поставил/создал» — заявление, а «могу добавить/поставить» — предложение
 * (его варнинг не должен ловить). Стемы прошедшего покрывают род/число (создал/создала/создали).
 */
const ACTION_VERB = /(создал|добавил|поставил|запланировал|удалил|отменил|перенёс|сдвинул)/i;
/** Существительные, привязывающие заявление к планировщику. */
const SCHEDULER_NOUN = /(напоминан|задач|планировщик)/i;

/**
 * Похоже ли, что ответ заявляет о выполненном действии в планировщике (создал/удалил и т.п.), хотя
 * за весь ход НЕ вызывался НИ ОДИН инструмент — признак чистой галлюцинации (модель «рапортует» и
 * выдумывает ID, ничего не сделав). Сознательно консервативно: если вызывался хоть какой-то
 * инструмент (планировщика или иной, напр. запись в файл), ответ опирается на реальную работу —
 * иначе по словам не отличить «добавил [в файл]» от «добавил [напоминание]» и варнинг ложно срабатывает.
 */
export function claimsSchedulerActionWithoutCall(answer: string, calledTools: string[]): boolean {
  return calledTools.length === 0 && ACTION_VERB.test(answer) && SCHEDULER_NOUN.test(answer);
}
