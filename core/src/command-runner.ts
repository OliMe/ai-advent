/**
 * Шов запуска команд проекта: пайплайну (этапу проверки) нужно ПРОГНАТЬ команду проекта
 * (тесты/сборку/линтер) и увидеть код выхода с выводом. Ядро определяет только контракт —
 * реальный запуск процесса (child_process) даёт вызывающий слой (llm-cli), чтобы ядро не
 * порождало процессов и оставалось тестируемым с фейком.
 */

/** Результат запуска команды: сама команда, код выхода, потоки и флаг таймаута. */
export interface CommandResult {
  /** Запущенная команда (как строка) — для показа и сопоставления в замечаниях. */
  command: string;
  /** Код выхода процесса; ненулевой — команда провалилась. */
  code: number;
  stdout: string;
  stderr: string;
  /** true — команда прервана по таймауту (код выхода в этом случае не показателен). */
  timedOut: boolean;
}

/** Параметры одного запуска: рабочий каталог обязателен, таймаут и доп. окружение — опциональны. */
export interface CommandRunOptions {
  /** Рабочий каталог, в котором исполняется команда (для пайплайна — копия проекта). */
  cwd: string;
  /** Потолок времени выполнения в мс; истёк — процесс убивается, `timedOut: true`. */
  timeoutMs?: number;
  /**
   * Доп. переменные окружения поверх `process.env` (напр. загруженные из `.env`/`.env.development`
   * проекта) — чтобы команды сборки/тестов видели требуемые переменные (`BUILD_PUBLIC_BASE` и т.п.).
   */
  env?: Record<string, string>;
}

/**
 * Запуск команды проекта (реализуется поверх child_process в llm-cli). Имя с префиксом `Project`,
 * чтобы не пересекаться с локальным `CommandRunner` llm-cli (синхронный запуск для буфера обмена).
 */
export interface ProjectCommandRunner {
  /** Исполняет команду в заданном каталоге; никогда не бросает — провал отражается в результате. */
  run(command: string, options: CommandRunOptions): Promise<CommandResult>;
}
