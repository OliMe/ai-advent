/**
 * Описание одного инструмента, доступного агенту: имя, описание и JSON-схема входных
 * параметров. Провайдеро-независимо; конкретный формат запроса собирает клиент/диалог.
 */
export interface ToolSpec {
  name: string;
  description: string;
  parameters: Record<string, unknown>;
}

/**
 * Набор инструментов для агента (источник tool-use). Реализуется адаптерами (например,
 * MCP-клиентом). Ядро знает только этот узкий контракт — не зависит ни от какого SDK.
 */
export interface ToolSet {
  /** Доступные инструменты (их описания для модели). */
  specs(): ToolSpec[];
  /** Выполняет инструмент по имени с аргументами; возвращает текстовый результат. */
  call(name: string, args: Record<string, unknown>): Promise<string>;
}
