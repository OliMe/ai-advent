import type { ToolSet } from './tool-set.ts';
import type { ProjectCommands } from './project-context.ts';
import type { CommandResult } from './command-runner.ts';

/**
 * Абстракции работы пайплайна с файлами проекта и запуском его команд (День 34). Определены в
 * ядре, потому что ими пользуются РАННЕРЫ ЭТАПОВ (execution/verification), а реализуются в llm-cli
 * поверх git-worktree и child_process — ядро не порождает процессов и не знает про git.
 *
 * Смысл разделения: execution даёт агенту РЕАЛЬНО менять файлы (в изолированной копии проекта) и
 * снимает результат как diff; verification прогоняет ОБНАРУЖЕННЫЕ команды проекта на этих изменениях.
 * Оба поля `StageContext` опциональны: не заданы (проект не привязан) — этапы работают как прежде.
 */

/** Сводка изменений, внесённых агентом в файловое пространство: unified diff + список путей. */
export interface WorkspaceChangeSummary {
  /** Unified diff всех правок (для показа пользователю и передачи проверяющему). */
  diff: string;
  /** Пути изменённых файлов относительно корня проекта (create/modify/delete). */
  files: string[];
}

/**
 * Файловое пространство этапа выполнения: набор инструментов (чтение/поиск/создание/изменение
 * файлов), которым агент работает с проектом, и снимок внесённых изменений. Инструменты
 * скоупятся на изолированную копию — рабочее дерево не трогается до применения.
 */
export interface FileWorkspace {
  /** Инструменты работы с файлами для агента-исполнителя (function-calling). */
  tools: ToolSet;
  /** Снимает текущие изменения пространства: unified diff + список изменённых файлов. */
  changeSummary(): Promise<WorkspaceChangeSummary>;
}

/**
 * Запуск команд проекта для этапа проверки: какие команды известны (`ProjectCommands`) и как
 * прогнать выбранную. Прогоняются только обнаруженные команды проекта — allow-list по построению.
 */
export interface CommandCheck {
  /** Обнаруженные команды проекта (test/build/lint/start) — что вообще можно запустить. */
  commands: ProjectCommands;
  /** Прогоняет команду и возвращает результат (код выхода + вывод); не бросает. */
  run(command: string): Promise<CommandResult>;
}
