/** Режим переписывания запроса: `none` — без изменений, `expand` — обогащение, `hyde` — гипотетический документ. */
export type RewriteMode = 'none' | 'expand' | 'hyde';

/** Обёртка над chat-моделью: system + user промпты → текст ответа. Инъектируется (тестируемо). */
export type ChatComplete = (systemPrompt: string, userPrompt: string) => Promise<string>;

/** Промпт expand на языке запроса (язык корпуса не определён — фолбэк). */
const EXPAND_SAME_LANGUAGE =
  'Ты помогаешь поиску по документации. По запросу пользователя выпиши через запятую ' +
  'ключевые синонимы, смежные термины и переформулировки (без пояснений, одной строкой) — ' +
  'чтобы расширить охват поиска. Отвечай на языке запроса.';

/** Промпт HyDE на языке запроса (язык корпуса не определён — фолбэк). */
const HYDE_SAME_LANGUAGE =
  'Ты помогаешь поиску по документации методом HyDE. Напиши краткий (2–4 предложения) ' +
  'правдоподобный фрагмент документации, который бы отвечал на запрос пользователя, как будто ' +
  'он взят из искомого документа. Без оговорок и вступлений, только сам фрагмент. Язык — как в запросе.';

/**
 * expand на ЯЗЫК КОРПУСА: синонимы/термины на языке документации (перевод при необходимости) — чтобы
 * расширение матчило английские (и т.п.) доки, а не оставалось на языке вопроса.
 */
function expandForLanguage(language: string): string {
  return (
    'Ты помогаешь поиску по документации. По запросу пользователя выпиши через запятую ключевые ' +
    `синонимы, смежные термины и переформулировки НА ЯЗЫКЕ «${language}» (язык документации; если ` +
    'запрос на другом языке — переведи), без пояснений, одной строкой — чтобы расширить охват поиска.'
  );
}

/**
 * HyDE на ЯЗЫК КОРПУСА: сперва перевести смысл запроса, затем написать гипотетический фрагмент на
 * языке документации — закрывает кросс-язычный промах (русский вопрос ↔ английские доки).
 */
function hydeForLanguage(language: string): string {
  return (
    'Ты помогаешь поиску по документации методом HyDE. Сначала переведи смысл запроса на язык ' +
    `«${language}» (язык документации), затем напиши краткий (2–4 предложения) правдоподобный ` +
    `фрагмент документации НА ЯЗЫКЕ «${language}», который отвечал бы на запрос, как будто взят из ` +
    'искомого документа. Без оговорок и вступлений — только сам фрагмент.'
  );
}

/** System-промпт под режим и целевой язык: задан язык корпуса → кросс-язычный вариант, иначе — «как в запросе». */
function systemPromptFor(mode: 'expand' | 'hyde', targetLanguage: string | undefined): string {
  const language = targetLanguage?.trim();
  if (language === undefined || language === '') {
    return mode === 'expand' ? EXPAND_SAME_LANGUAGE : HYDE_SAME_LANGUAGE;
  }
  return mode === 'expand' ? expandForLanguage(language) : hydeForLanguage(language);
}

/**
 * Собирает функцию переписывания запроса под режим (only expand/hyde — режим `none` вызывающий код
 * не передаёт). `expand` дописывает к запросу обогащение, `hyde` заменяет запрос гипотетическим
 * документом; при пустом ответе модели — исходный запрос без изменений. `targetLanguage` (язык
 * корпуса) — переводит/генерирует rewrite НА ЭТОТ ЯЗЫК; не задан — прежнее поведение «на языке запроса».
 */
export function makeRewriter(
  mode: 'expand' | 'hyde',
  complete: ChatComplete,
  targetLanguage?: string,
): (query: string) => Promise<string> {
  const systemPrompt = systemPromptFor(mode, targetLanguage);
  return async query => {
    const reply = (await complete(systemPrompt, query)).trim();
    if (!reply) {
      return query;
    }
    return mode === 'expand' ? `${query}\n${reply}` : reply;
  };
}
