/** Персона — это URL, модель и системный промпт. Всё остальное у них общее. */
export interface Persona {
  /** Сегмент пути: /grumpy, /kitchen. */
  slug: string;
  /** Заголовок для интерфейса. */
  title: string;
  /** Имя модели в Ollama. */
  model: string;
  /** Базовый системный промпт (к нему добавляется тон из настроения). */
  systemPrompt: string;
  /** Подпись под полем ввода — что сюда писать. */
  inputHint: string;
}

/**
 * Модели выбраны замером, а не на глаз: llama3.2:3b взяла 5/5 фактических вопросов,
 * qwen2.5:3b на них ошибалась (звала столицей Австралии Анкару), зато на генеративном
 * русском дала 1 латинское вкрапление против 8 у llama.
 */
const GRUMPY_NODE: Persona = {
  slug: 'grumpy',
  title: 'Ворчливый узел',
  model: 'llama3.2-3b',
  systemPrompt:
    'Ты — дух сервера, на котором тебя запустили: маленькая модель на 3 миллиарда параметров, ' +
    'живущая на VPS с четырьмя ядрами, без видеокарты, рядом с другими сервисами. ' +
    'Ты отвечаешь на вопросы пользователя честно и по-русски. ' +
    'Ты знаешь, что ты небольшая модель и можешь ошибаться — не притворяйся всеведущим.',
  inputHint: 'Спросите что-нибудь у сервера',
};

const KITCHEN: Persona = {
  slug: 'kitchen',
  title: 'Что в холодильнике',
  model: 'qwen2.5:3b',
  systemPrompt:
    'Ты — повар. По списку продуктов предложи ОДНО блюдо, которое из них можно приготовить. ' +
    'Формат ответа: название блюда, список ингредиентов, затем 3–5 шагов приготовления. ' +
    'Используй преимущественно те продукты, что назвал пользователь. ' +
    'Отвечай ТОЛЬКО на русском языке, без английских слов.',
  inputHint: 'Перечислите, что нашли в холодильнике',
};

/** Все персоны сервиса. */
export const PERSONAS: readonly Persona[] = [GRUMPY_NODE, KITCHEN];

/** Ищет персону по сегменту пути; неизвестный slug — undefined. */
export function findPersona(slug: string): Persona | undefined {
  return PERSONAS.find(persona => persona.slug === slug);
}

/** Склеивает системный промпт персоны с тоном, продиктованным настроением узла. */
export function composeSystemPrompt(persona: Persona, toneInstruction: string): string {
  return `${persona.systemPrompt}\n\n${toneInstruction}`;
}
