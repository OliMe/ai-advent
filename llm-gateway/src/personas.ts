/** Персона — это URL, модель, системный промпт и профиль генерации. */
export interface Persona {
  /** Сегмент пути: /kitchen. */
  slug: string;
  /** Заголовок для интерфейса. */
  title: string;
  /** Имя модели в Ollama. */
  model: string;
  /** Системный промпт. */
  systemPrompt: string;
  /** Подпись под полем ввода — что сюда писать. */
  inputHint: string;
  /** Температура сэмплирования: рецепт — задача творческая, но не бредовая. */
  temperature: number;
  /** Потолок длины ответа в токенах: рецепту хватает ~350, берём с запасом. */
  maxTokens: number;
}

/**
 * Модель выбрана замером: на генеративном русском `qwen2.5:3b` дала 1 латинское вкрапление
 * против 8 у `llama3.2:3b` (та вставляла в рецепты «golden brown», «切ños» и выдумывала
 * «Смоковинную цыпленку»). На фактических вопросах расклад обратный, но рецепту факты не нужны:
 * у него нет одного правильного ответа, и выдумка здесь — продукт, а не дефект.
 */
const KITCHEN: Persona = {
  slug: 'kitchen',
  title: 'Что в холодильнике',
  model: 'qwen2.5:3b',
  systemPrompt:
    'Ты — повар. По списку продуктов предложи ОДНО блюдо, которое из них можно приготовить. ' +
    'Формат ответа: название блюда, список ингредиентов, затем 3–5 шагов приготовления. ' +
    'Используй преимущественно те продукты, что назвал пользователь. ' +
    'Отвечай ТОЛЬКО на русском языке, без английских слов.',
  inputHint: 'Перечислите, что нашли в холодильнике: яйца, картошка, лук, сметана…',
  temperature: 0.7,
  maxTokens: 500,
};

/** Все персоны сервиса. */
export const PERSONAS: readonly Persona[] = [KITCHEN];

/** Персона по умолчанию — на неё ведёт корень сервиса. */
export const DEFAULT_PERSONA: Persona = KITCHEN;

/** Ищет персону по сегменту пути; неизвестный slug — undefined. */
export function findPersona(slug: string): Persona | undefined {
  return PERSONAS.find(persona => persona.slug === slug);
}
